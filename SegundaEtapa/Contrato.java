
public class Contrato {

	int mesesCont;
	int qtdeForn;
	int fornecedor;
	int mesIni, mesFim;
	float valorTotal;
	
	
	public Contrato() {
		
		
	}


	public int getMesesCont() {
		return mesesCont;
	}


	public void setMesesCont(int mesesCont) {
		this.mesesCont = mesesCont;
	}


	public int getQtdeForn() {
		return qtdeForn;
	}


	public void setQtdeForn(int qtdeForn) {
		this.qtdeForn = qtdeForn;
	}


	public int getFornecedor() {
		return fornecedor;
	}


	public void setFornecedor(int fornecedor) {
		this.fornecedor = fornecedor;
	}


	public int getMesIni() {
		return mesIni;
	}


	public void setMesIni(int mesIni) {
		this.mesIni = mesIni;
	}


	public int getMesFim() {
		return mesFim;
	}


	public void setMesFim(int mesFim) {
		this.mesFim = mesFim;
	}


	public float getValorTotal() {
		return valorTotal;
	}


	public void setValorTotal(float valorTotal) {
		this.valorTotal = valorTotal;
	}

	
	
	
	

}
